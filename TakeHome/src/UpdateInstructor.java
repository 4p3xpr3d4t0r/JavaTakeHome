    private void removeActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_removeActionPerformed
        try{
            Class.forName("com.mysql.jdbc.Driver");
            Connection con = DriverManager.getConnection("jdbc:mysql://localhost:3308/nsbm","root","123456");
            
            
            int row = table.getSelectedRow();
            String value = (table.getModel().getValueAt(row,0).toString());
            table.setColumnSelectionAllowed(true);
            String query = "delete from instructor where indexno='"+value+"'";
            PreparedStatement pst = con.prepareStatement(query);
            pst.executeUpdate();
            
            resetActionPerformed(evt);
            JOptionPane.showMessageDialog(null,"Instructor Has Been Deleted Successfully!");
            
            
        } 
        catch(Exception e){
            JOptionPane.showMessageDialog(null,e);
        }
    }//GEN-LAST:event_removeActionPerformed

