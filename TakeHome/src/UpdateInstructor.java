    private void updateActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_updateActionPerformed
        try{
            Class.forName("com.mysql.jdbc.Driver");
            Connection con = DriverManager.getConnection("jdbc:mysql://localhost:3308/nsbm","root","123456");
            int row = table.getSelectedRow();
            String value = (table.getModel().getValueAt(row,0).toString());
            table.setColumnSelectionAllowed(true);
            String query = "update instructor set indexNo=?,name=?,address=?,nic=?,dob=?,room=?,faculty=?,gender=?,lab=?,phoneno=?,subtitle=? where indexNo='"+value+"'";
            PreparedStatement pst = con.prepareStatement(query);
            pst.setString(1,indexNo.getText().toUpperCase());
            pst.setString(2,name.getText().toUpperCase());
            pst.setString(3,address.getText().toUpperCase());
            pst.setString(4,nic.getText().toUpperCase());
            pst.setString(5,dob.getText());
            pst.setString(6,room.getText());
            
            String gender;
            if(male.isSelected()){
                gender = "Male";
            }
            else{
                gender = "Female";
            }
            pst.setString(8,gender);
            String study;
            study = faculty.getSelectedItem().toString();
            pst.setString(7,study);
            pst.setString(9,lab.getText());
            pst.setString(10,phoneNo.getText());
            pst.setString(11,title.getText().toUpperCase());
            pst.executeUpdate();
            JOptionPane.showMessageDialog(null,"Instructor Has Been Updated Successfully!");
            
            
        } 
        catch(Exception e){
            JOptionPane.showMessageDialog(null,e);
        }
    }//GEN-LAST:event_updateActionPerformed

