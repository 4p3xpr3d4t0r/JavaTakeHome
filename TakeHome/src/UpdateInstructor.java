    private void searchActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_searchActionPerformed
        try{
            Class.forName("com.mysql.jdbc.Driver");
            Connection con = DriverManager.getConnection("jdbc:mysql://localhost:3308/nsbm","root","123456");
            String str = index.getText().toUpperCase();
            String query1 = ("select * from instructor where indexNo='"+str+"';");
            
            Statement st = con.createStatement();
            ResultSet rs = st.executeQuery(query1);
            rs.next();
            Instructor pgs = new Instructor(rs.getString("indexNo"),rs.getString("name"),rs.getString("address"),rs.getString("nic"),rs.getString("dob"),rs.getString("room"),rs.getString("faculty"),rs.getString("gender"),rs.getString("lab"),rs.getInt("phoneno"),rs.getString("subtitle"));
            
            DefaultTableModel model = (DefaultTableModel)table.getModel();
            Object[] row = new Object[10];
            row[0] = pgs.getIndexNo();
            row[1] = pgs.getName();
            row[2] = pgs.getAddress();
            row[3] = pgs.getNic();
            row[4] = pgs.getDob();
            row[5] = pgs.getRoom();
            row[6] = pgs.getFaculty();
            row[7] = pgs.getGender();
            row[8] = pgs.getLab();
            row[9] = pgs.getPhoneNo();
            row[10] = pgs.getSubject();
            
            model.addRow(row);
            
        }
        
        catch(Exception e){
             JOptionPane.showMessageDialog(null,e);
        }
    }//GEN-LAST:event_searchActionPerformed

