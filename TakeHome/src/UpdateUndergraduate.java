    private void updateActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_updateActionPerformed
        try{
            Class.forName("com.mysql.jdbc.Driver");
            Connection con = DriverManager.getConnection("jdbc:mysql://localhost:3308/nsbm","root","123456");
            
            
            int row = table.getSelectedRow();
            String value = (table.getModel().getValueAt(row,0).toString());
            table.setColumnSelectionAllowed(true);
            String query = "update bachelorstudents set indexNo=?,name=?,address=?,nic=?,dob=?,gender=?,course=?,intake=?,year=?,faculty=?,rankofal=?,streamofal=?,phoneno=? where indexNo='"+value+"'";
            PreparedStatement pst = con.prepareStatement(query);
            pst.setString(1,indexNo.getText().toUpperCase());
            pst.setString(2,name.getText().toUpperCase());
            pst.setString(3,address.getText().toUpperCase());
            pst.setString(4,nic.getText().toUpperCase());
            pst.setString(5,dob.getText());
            pst.setString(7,course.getText());
            pst.setString(8,intake.getText().toUpperCase());
            pst.setString(9,year.getText());
            String gender;
            if(male.isSelected()){
                gender = "Male";
            }
            else{
                gender = "Female";
            }
            pst.setString(6,gender);
            String study;
            study = faculty.getSelectedItem().toString();
            pst.setString(10,study);
            pst.setString(11,alRank.getText());
            String stream;
            stream = alStream.getSelectedItem().toString();
            pst.setString(12,stream);
            pst.setString(13,phoneNo.getText());
            pst.executeUpdate();
            
            
            JOptionPane.showMessageDialog(null,"Updated Successfully!");
            
            
        } 
        catch(Exception e){
            JOptionPane.showMessageDialog(null,e);
        }
        
        
        
        
    }//GEN-LAST:event_updateActionPerformed
    
    
    private void tableMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_tableMouseClicked
        int index = table.getSelectedRow();
        TableModel model = table.getModel();
        indexNo.setText(model.getValueAt(index,0).toString());
        name.setText(model.getValueAt(index,1).toString());
        address.setText(model.getValueAt(index,2).toString());
        nic.setText(model.getValueAt(index,3).toString());
        dob.setText(model.getValueAt(index,4).toString());
        intake.setText(model.getValueAt(index,7).toString());
        year.setText(model.getValueAt(index,8).toString());
        
        alRank.setText(model.getValueAt(index,10).toString());
        
        phoneNo.setText(model.getValueAt(index,12).toString());
        
        course.setText(model.getValueAt(index,6).toString());
        
        String strFaculty = model.getValueAt(index,9).toString();
        String strStream = model.getValueAt(index,11).toString();
        String strGender = model.getValueAt(index,5).toString();
        
        
        if("Male".equals(strGender)){
            male.setSelected(true);
        }
        else{
            female.setSelected(true);
        }
        
        
        if("School Of Computing".equals(strFaculty)){
            faculty.setSelectedIndex(0);
        }
        else if("School Of Engineering".equals(strFaculty)){
            faculty.setSelectedIndex(1);
        }
        else{
            faculty.setSelectedIndex(2);
        }
        
        
        if(null == strStream){
            alStream.setSelectedIndex(4);
        }
        else switch (strStream) {
            case "Mathematics":
                alStream.setSelectedIndex(0);
                break;
            case "Biology":
                alStream.setSelectedIndex(1);
                break;
            case "Technology":
                alStream.setSelectedIndex(2);
                break;
            case "Arts":
                alStream.setSelectedIndex(3);
                break;
            default:
                alStream.setSelectedIndex(4);
                break;
        }
        
        
    }//GEN-LAST:event_tableMouseClicked

