    private void signinActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_signinActionPerformed
        try{
            Class.forName("com.mysql.jdbc.Driver");
            Connection con = DriverManager.getConnection("jdbc:mysql://localhost:3308/nsbm","root","123456");
            String sql = "select * from userdetails where username=? and password=?";
            PreparedStatement pst = con.prepareStatement(sql);
            pst.setString(1,username.getText());
            pst.setString(2,password.getText());
            
            ResultSet rs = pst.executeQuery();
            if(rs.next()){
                //JOptionPane.showMessageDialog(null,"Successfully Signed in");
                this.hide();
                AdminMainMenu menu = new AdminMainMenu();
                menu.setVisible(true);
                setVisible(false);
                
            }
            else{
                JOptionPane.showMessageDialog(null,"Username or Password Mismatch");
                username.setText("");
                password.setText("");
            
            }
        }
        catch(Exception e){
            JOptionPane.showMessageDialog(null,e);
        }
        
    }//GEN-LAST:event_signinActionPerformed

