    private void updateActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_updateActionPerformed
        try{
            Class.forName("com.mysql.jdbc.Driver");
            Connection con = DriverManager.getConnection("jdbc:mysql://localhost:3308/nsbm","root","123456");
            
            
            int row = table.getSelectedRow();
            String value = (table.getModel().getValueAt(row,0).toString());
            table.setColumnSelectionAllowed(true);
            String query = "update postgraduate set indexNo=?,name=?,address=?,nic=?,dob=?,institute=?,qualificationtype=?,gender=?,course=?,yearofgraduate=?,intake=?,year=? where indexNo='"+value+"'";
            PreparedStatement pst = con.prepareStatement(query);
            pst.setString(1,indexNo.getText().toUpperCase());
            pst.setString(2,name.getText().toUpperCase());
            pst.setString(3,address.getText().toUpperCase());
            pst.setString(4,nic.getText().toUpperCase());
            pst.setString(5,dob.getText());
            pst.setString(7,qualificationType.getText());
            pst.setString(11,intake.getText().toUpperCase());
            pst.setString(9,year.getText());
            String gender;
            if(male.isSelected()){
                gender = "Male";
            }
            else{
                gender = "Female";
            }
            pst.setString(8,gender);
            String study;
            study = course.getSelectedItem().toString();
            pst.setString(9,study);
            pst.setString(6,institute.getText());
            pst.setString(10,yearOfComplete.getText());
            pst.setString(12,year.getText());
            pst.executeUpdate();
            
            
            JOptionPane.showMessageDialog(null,"Updated Successfully!");
            
            
        } 
        catch(Exception e){
            JOptionPane.showMessageDialog(null,e);
        }
    }//GEN-LAST:event_updateActionPerformed

