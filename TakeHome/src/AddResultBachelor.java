    private void addResultsActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_addResultsActionPerformed
        
        try{
            String result="";
            int ans;
            double gpa=0.0;
            int total = 0;
            int count=0;
            int mark = Integer.parseInt(marks.getText());
            
            
            Class.forName("com.mysql.jdbc.Driver");
            Connection con = DriverManager.getConnection("jdbc:mysql://localhost:3308/nsbm","root","123456");
            
            
            String query1 = "select sum(results) as total from bachelorassignment where indexNo='"+indexNo.getText().toUpperCase()+"' and title='"+title.getText().toUpperCase()+"'";
            
            Statement st = con.createStatement();
            ResultSet rs = st.executeQuery(query1);
            
            if(rs.next()){
                total = rs.getInt("total");
            }
            
            Statement st1 = con.createStatement();
            
                
            
            String query2 = "select count(results) as co from bachelorassignment where indexNo='"+indexNo.getText().toUpperCase()+"' and title='"+title.getText().toUpperCase()+"'";
            ResultSet rs1 = st1.executeQuery(query2);
            if(rs1.next()){
                count = rs1.getInt("co");
            }
            
            double average = (double)total/count;
            int percentage=0;
            
            Statement st2 = con.createStatement();
            String query3 = "select assignment from subject where title='"+title.getText().toUpperCase()+"'";
            ResultSet rs3 = st2.executeQuery(query3);
            
            if(rs3.next()){
                percentage = Integer.parseInt(rs3.getString("assignment"));
            }
            
            int last = (int)((percentage*average)+((100-percentage)*mark));
            ans = last/100;
            
            if(ans<=100 && ans>=75){
                result = "A";
                gpa = 4.0;
            }
            else if(ans<75 && ans>=65){
                result = "B";
                gpa = 3.5;
            }
            else if(ans<65 && ans>=55){
                result = "C";
                gpa = 3.0;
            }
            else if(ans<55 && ans>=45){
                result = "D";
                gpa = 2.5;
            }
            else if(ans<45 && ans>=35){
                result = "E";
                gpa = 2.0;
            }
            else{
                result = "F";
                gpa = 0.0;
            }
            
            String query = "update bachelorsubject set result='"+result+"',gpa="+gpa+"where indexno=? and title=?";
            PreparedStatement pst = con.prepareStatement(query);
            pst.setString(1,indexNo.getText().toUpperCase());
            pst.setString(2,title.getText().toUpperCase());
            pst.executeUpdate();
            
            
            //getting sum of gpa from all subjects
            Statement st4 = con.createStatement();
            String query4 = "select sum(gpa) as total from bachelorsubject where indexno='"+indexNo.getText().toUpperCase()+"'";
            ResultSet rs4 = st4.executeQuery(query4);
            double totalGpa = 0;
            if(rs4.next()){
                totalGpa = rs4.getDouble("total");
            }
            
            
           
            
            
            //taking number of subjects to find the average of Gpa
            Statement st5 = con.createStatement();
            String query5 = "select count(gpa) as num from bachelorsubject where indexno='"+indexNo.getText().toUpperCase()+"'";
            ResultSet rs5 = st5.executeQuery(query5);
            int countGpa = 0;
            if(rs5.next()){
                countGpa = rs5.getInt("num");
            }
            
            
            double finalGpa = totalGpa/countGpa;
            
            //updating the gpa for the semester for the current student
            String query6 = "update bachelorsemester set gpa=? where indexno='"+indexNo.getText().toUpperCase()+"'";
            PreparedStatement pst6 = con.prepareStatement(query6);
            pst6.setDouble(1,finalGpa);
            pst6.executeUpdate();
            
            
            //taking all the semester gpa
            Statement st7 = con.createStatement();
            String query7 = "select sum(gpa) as gpa from bachelorsemester where indexno='"+indexNo.getText().toUpperCase()+"'";
            ResultSet rs7 = st7.executeQuery(query7);
            double lastGpa = 0;
            if(rs7.next()){
                lastGpa = rs7.getDouble("gpa");
            }
            
            
            //taking number of semester to find the total average gpa
            Statement st8 = con.createStatement();
            String query8 = "select count(gpa) as gpa from bachelorsemester where indexno='"+indexNo.getText().toUpperCase()+"'";
            ResultSet rs8 = st7.executeQuery(query7);
            int totalCount = 0;
            if(rs7.next()){
                totalCount = rs7.getInt("gpa");
            }
            
            double addGpa = lastGpa/totalCount;
            
            
            //updating the current gpa for the student
            String query9 = "update bachelorstudents set gpa=? where indexno='"+indexNo.getText().toUpperCase()+"'";
            PreparedStatement pst1 = con.prepareStatement(query9);
            pst1.setDouble(1,addGpa);
            pst1.executeUpdate();
            
            //ranking
            ArrayList<BachelorStudent> students = new ArrayList<>();
            Statement st10 = con.createStatement();
            String query10 = "select * from bachelorstudents where indexno='"+indexNo.getText().toUpperCase()+"' and course='"+course.getText().toUpperCase()+"'";
            ResultSet rs10 = st10.executeQuery(query10);
            while(rs10.next()){
                BachelorStudent bs = new BachelorStudent(rs10.getString("indexno"),rs10.getString("name"),rs10.getString("address"),rs10.getString("nic"),rs10.getString("dob"),rs10.getString("gender"),rs10.getString("course"),rs10.getString("intake"),rs10.getInt("year"),rs10.getString("faculty"),rs10.getString("rankofal"),rs10.getString("streamofal"),rs10.getInt("phoneno"),rs10.getInt("rank"),rs10.getDouble("gpa"));
                students.add(bs);
            }
            
            for(int i=0;i<students.size();i++){
                for(int j=0;j<students.size();j++){
                    if(students.get(i).getGpa()<students.get(j).getGpa()){
                        BachelorStudent temp = students.get(i);
                        students.set(i,students.get(j));
                        students.set(j,temp);
                    }
                }
            }
            
            int rank = 1;
            for(int i=0;i<students.size();i++){
                String index = students.get(i).getIndexNo();
                String query11 = "update bachelorstudents set rank=? where indexNo='"+index+"'";
                PreparedStatement pst2 = con.prepareStatement(query11);
                pst2.setInt(1,rank);
                pst2.executeUpdate();
                rank++;
                
            }
            
            
            
            
            
            JOptionPane.showMessageDialog(null,"Result Added Successfully!!!");
            
         }
         
         catch(Exception e){
             JOptionPane.showMessageDialog(null,e);
         }
        
    }//GEN-LAST:event_addResultsActionPerformed

