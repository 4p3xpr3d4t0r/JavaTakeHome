    private void searchActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_searchActionPerformed
        
        try{
            Class.forName("com.mysql.jdbc.Driver");
            Connection con = DriverManager.getConnection("jdbc:mysql://localhost:3308/nsbm","root","123456");
            int num = Integer.parseInt(userId.getText());
            String query1 = ("select * from userdetails where userid="+num+";");
            
             
            Statement st = con.createStatement();
            ResultSet rs = st.executeQuery(query1);
            rs.next();
            Admin admin = new Admin(rs.getInt("userid"),rs.getString("username"),rs.getString("password"));
            
            DefaultTableModel model = (DefaultTableModel)table.getModel();
            Object[] row = new Object[3];
            row[0] = admin.getUserId();
            row[1] = admin.getUserName();
            row[2] = admin.getPassword();
            model.addRow(row);
            
        }
        
        catch(Exception e){
             JOptionPane.showMessageDialog(null,e);
        }
    }//GEN-LAST:event_searchActionPerformed

