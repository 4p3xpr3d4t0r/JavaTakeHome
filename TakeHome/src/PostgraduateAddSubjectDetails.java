    private void addCompulsorySubjectsActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_addCompulsorySubjectsActionPerformed
        try{
                Class.forName("com.mysql.jdbc.Driver");
                Connection con = DriverManager.getConnection("jdbc:mysql://localhost:3308/nsbm","root","123456");
            
                String query = "insert into postgraduatestudentsubjects(indexno,title,result,gpa,courseid,semesterno)select semesterno,title,semesterno,credit,courseid,semesterno from postgraduatesubjects where semesterno=? and courseid=? and type='compulsory'";
                PreparedStatement pst1 = con.prepareStatement(query);
                pst1.setString(1,semester.getText());
                String study;
                study = course.getText().toUpperCase();
                pst1.setString(2,study);
                pst1.executeUpdate();
            
            
                String query3 = "update postgraduatestudentsubjects set indexno=?,gpa=? where indexno=?";
                PreparedStatement pst3 = con.prepareStatement(query3);
                pst3.setString(1,indexNo.getText().toUpperCase());
                pst3.setString(2,"0.0");
                pst3.setString(3,semester.getText());
            
                pst3.executeUpdate();
                
                String query2 = "select sum(fees) as total from postgraduatesubjects where semesterno="+Integer.parseInt(semester.getText())+" and type='compulsory'";
                Statement st = con.createStatement();
                ResultSet rs = st.executeQuery(query2);
                int fees = 0;
                if(rs.next()){
                    fees = rs.getInt("total");
                }
                
                
                String query4 = "insert into postgraduatesemester(indexno,number,payment) values(?,?,?)";
                PreparedStatement pst4 = con.prepareStatement(query4);
                pst4.setString(1,indexNo.getText().toUpperCase());
                pst4.setString(2,semester.getText());
                pst4.setInt(3,fees);
                pst4.executeUpdate();
                
                
                JOptionPane.showMessageDialog(null,"Compulsory Subjects for Semester "+semester.getText()+" has been added!!!");
            }
            catch(Exception e){
             JOptionPane.showMessageDialog(null,e);
        }
    }//GEN-LAST:event_addCompulsorySubjectsActionPerformed

    private void saveActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_saveActionPerformed
        try{
            Class.forName("com.mysql.jdbc.Driver");
            Connection con = DriverManager.getConnection("jdbc:mysql://localhost:3308/nsbm","root","123456");
            
            String query1 = "insert into postgraduatestudentsubjects(indexNo,title,result,gpa,courseid,semesterno)values(?,?,?,?,?,?)";
            
            PreparedStatement pst = con.prepareStatement(query1);
            pst.setString(1,indexNo.getText().toUpperCase());
            pst.setString(6,semester.getText());
            pst.setString(2,choose1.getText().toUpperCase());
            pst.setString(4,"0.0");
            pst.setString(3,"");
            String study;
            study = course.getText().toUpperCase();
            pst.setString(5,study);
            pst.executeUpdate();
            
           
                
            int fee = 0;
            
            String query3 = "select payment from postgraduatesemester where indexno='"+indexNo.getText().toUpperCase()+"' and number='"+semester.getText()+"'";
            Statement st = con.createStatement();
            ResultSet rs1 = st.executeQuery(query3);
            if(rs1.next()){
                fee = rs1.getInt("payment");
            }
            
            
            String query2 = "select sum(fees) as total from postgraduatesubjects where title='"+choose1.getText().toUpperCase()+"'";
            
            ResultSet rs = st.executeQuery(query2);
                
                if(rs.next()){
                    fee += rs.getInt("total");
                }
                
            String query4 = "update postgraduatesemester set payment=? where indexno='"+indexNo.getText().toUpperCase()+"'";
            PreparedStatement pst3 = con.prepareStatement(query4);
            pst3.setInt(1,fee);
            
            
            pst3.executeUpdate();
            
            
            
            
            JOptionPane.showMessageDialog(null,"Subjects Added Successfully!");
        }
        catch(Exception e){
             JOptionPane.showMessageDialog(null,e);
        }
    }//GEN-LAST:event_saveActionPerformed

